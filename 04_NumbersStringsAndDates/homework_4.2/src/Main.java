public class Main {
    public static void main(String[] args) {

        System.out.println("Максимальные и минимальные значения различных типов чисел:");
        System.out.println();
        System.out.println("Максимильное значение числа типа byte = " + Byte.MAX_VALUE);
        System.out.println("Минимальное значение числа типа byte = " + Byte.MIN_VALUE);
        System.out.println();
        System.out.println("Максимильное значение числа типа int = " + Integer.MAX_VALUE);
        System.out.println("Минимальное значение числа типа int = " + Integer.MIN_VALUE);
        System.out.println();
        System.out.println("Максимильное значение числа типа short = " + Short.MAX_VALUE);
        System.out.println("Минимальное значение числа типа short = " + Short.MIN_VALUE);
        System.out.println();
        System.out.println("Максимильное значение числа типа long = " + Long.MAX_VALUE);
        System.out.println("Минимальное значение числа типа long = " + Long.MIN_VALUE);
        System.out.println();
        System.out.println("Максимильное значение числа типа float = " + Float.MAX_VALUE);
        System.out.println("Минимальное значение числа типа float = " + -Float.MAX_VALUE);
        System.out.println();
        System.out.println("Максимильное значение числа типа double = " + Double.MAX_VALUE);
        System.out.println("Минимальное значение числа типа double = " + -Double.MAX_VALUE);
    }
}
